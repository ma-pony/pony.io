import fs from 'fs/promises'
import path from 'path'
import matter from 'gray-matter'

const CONFIG_PATH = 'workspace.config.json'
const GENERATED_NOTICE = '{/* Generated by scripts/sync-workspace.mjs. Do not edit directly. */}'

function expandHome(input) {
  if (!input) return input
  if (input === '~') return process.env.HOME
  if (input.startsWith('~/')) return path.join(process.env.HOME, input.slice(2))
  return input
}

function buildSlugMap(notes) {
  return new Map(
    notes.map((note) => [path.basename(note.source, path.extname(note.source)), note.slug])
  )
}

function wikilinkToMarkdown(slugMap, _match, target, label) {
  const [title, heading] = target.split('#')
  const slug = slugMap.get(title)
  const display = label || heading || title

  if (!slug) {
    return display
  }

  return `[${display}](/workspace/${slug}${heading ? `#${heading}` : ''})`
}

function normalizeBareAngleLinks(line) {
  return line.replace(/<((?:https?:|mailto:)[^>]+)>/g, '[$1]($1)')
}

function normalizeTags(line) {
  return line.replace(/(^|\s)#([\p{Script=Han}\p{Letter}\p{Number}_/-]+)/gu, '$1`#$2`')
}

function filterExcludedSections(content, excludedHeadings = []) {
  if (!excludedHeadings.length) return content

  const excluded = new Set(excludedHeadings)
  const lines = content.split('\n')
  const output = []
  let excludedLevel = null

  for (const line of lines) {
    const match = line.match(/^(#{2,6})\s+(.*)$/)
    if (match) {
      const level = match[1].length
      const heading = match[2].replace(/#+$/, '').trim()
      const normalizedHeading = heading.replace(/^\d+(?:\.\d+)*\.?\s*/, '')

      if (excluded.has(heading) || excluded.has(normalizedHeading)) {
        excludedLevel = level
        continue
      }

      if (excludedLevel && level <= excludedLevel) {
        excludedLevel = null
      }
    }

    if (!excludedLevel) {
      output.push(line)
    }
  }

  return output.join('\n')
}

function transformBody(raw, sourceName, slugMap, excludedHeadings) {
  const parsed = matter(raw)
  const body = filterExcludedSections(parsed.content, excludedHeadings)
    .replace(/^#\s+.*\n+/, '')
    .replace(/!\[\[([^\]]+)\]\]/g, (_, target) => `**附件：** ${target}`)
    .replace(/\[\[([^|\]]+)(?:\|([^\]]+))?\]\]/g, (...args) => wikilinkToMarkdown(slugMap, ...args))
    .split('\n')
    .map(normalizeBareAngleLinks)
    .map(normalizeTags)
    .join('\n')
    .trim()

  return `${GENERATED_NOTICE}\n\n> 来源：Obsidian / ${sourceName}。这是远程阅读版，只同步白名单内容。\n\n${body}\n`
}

function redact(content, patterns) {
  return patterns.reduce((current, pattern) => {
    const ignoreCase = pattern.startsWith('(?i)')
    const source = ignoreCase ? pattern.slice(4) : pattern
    const regex = new RegExp(source, ignoreCase ? 'giu' : 'gu')
    return current.replace(regex, '[REDACTED]')
  }, content)
}

function frontmatterFor(note, date) {
  return matter.stringify('', {
    title: note.title,
    date: date.toISOString(),
    summary: note.summary,
    tags: ['workspace', 'AI Agent', '求职'],
    authors: ['default'],
    layout: 'WorkspaceLayout',
    draft: false,
    noindex: true,
  })
}

async function cleanOutputDir(outputDir) {
  await fs.rm(outputDir, { recursive: true, force: true })
  await fs.mkdir(outputDir, { recursive: true })
}

async function main() {
  const configRaw = await fs.readFile(CONFIG_PATH, 'utf8')
  const config = JSON.parse(configRaw)
  const vaultPath =
    expandHome(process.env[config.vaultPathEnv]) || expandHome(config.fallbackVaultPath)
  const outputDir = path.resolve(config.outputDir)
  const slugMap = buildSlugMap(config.notes)

  await cleanOutputDir(outputDir)

  const indexItems = []
  const sourceDates = []
  for (const note of config.notes) {
    const sourcePath = path.join(vaultPath, note.source)
    const raw = await fs.readFile(sourcePath, 'utf8')
    const stats = await fs.stat(sourcePath)
    sourceDates.push(stats.mtimeMs)
    const transformed = redact(
      transformBody(raw, note.source, slugMap, config.excludedHeadings || []),
      config.redactPatterns || []
    )
    const output = `${frontmatterFor(note, stats.mtime)}${transformed}`
    const outPath = path.join(outputDir, `${note.slug}.mdx`)
    await fs.writeFile(outPath, output, 'utf8')
    indexItems.push(`- [${note.title}](/workspace/${note.slug})：${note.summary}`)
  }

  const indexFrontmatter = matter.stringify('', {
    title: 'AI Agent Workspace',
    date: new Date(Math.max(...sourceDates)).toISOString(),
    summary: '从 Obsidian 白名单同步出来的 AI Agent 求职、学习和作品集远程阅读入口。',
    tags: ['workspace', 'AI Agent', '求职'],
    authors: ['default'],
    layout: 'WorkspaceLayout',
    draft: false,
    noindex: true,
  })

  const indexBody = `${GENERATED_NOTICE}\n\n这是从 Obsidian 白名单同步出来的远程阅读入口，不进入首页、RSS、站点地图和搜索引擎索引。\n\n## 页面\n\n${indexItems.join(
    '\n'
  )}\n`
  await fs.writeFile(path.join(outputDir, 'index.mdx'), `${indexFrontmatter}${indexBody}`, 'utf8')

  console.log(
    `Workspace synced: ${config.notes.length + 1} files -> ${path.relative(
      process.cwd(),
      outputDir
    )}`
  )
}

main().catch((error) => {
  console.error(error)
  process.exit(1)
})
